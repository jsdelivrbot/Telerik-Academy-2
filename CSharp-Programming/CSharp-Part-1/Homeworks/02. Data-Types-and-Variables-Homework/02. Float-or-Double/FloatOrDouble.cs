            float floatVariable3 = 34.567839023f;
            Console.WriteLine(doubleVariable1);
            Console.WriteLine(doubleVariable2);
            Console.WriteLine(floatVariable1);
            Console.WriteLine(floatVariable2);
            Console.WriteLine(floatVariable3);

        }
    }
}
